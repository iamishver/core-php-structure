                            <h3 class="inner-heading">NORLINX Company History</h3>
                            <p class="comp-para">NORLINX started over 25 years ago as a software developer for retail and electronic business applications.  Near the end of 1999, we were approached by the data center industry’s first intelligent power strip manufacturer with an opportunity that would change the direction and focus of our company. </p>
                            <p class="comp-para">In short, the opportunity was to create an enterprise software solution for managing thousands of data center power strips (PDU’s).  After close evaluation, and obtaining a broader understanding of the data center industry, we embarked on a journey to create a unique value proposition that would go beyond the integration and management of data center power strips exclusively.</p>
                            <p class="comp-para">After meeting with dozens of data centers and industry providers, we discovered a clear demand for software that could help create higher resiliency for IT equipment and facility infrastructure – all managed under a single pane of glass.  During the year 2000, NORLINX launched Global Site Management (GSM) and began to address critical questions within the data center, such as: </p>
                            <ul class="custom_today">
                                <li>What is my data center’s current, stranded and future capacity?
                                </li>
                                <li>Can I properly failover within each cabinet?</li>
                                <li>How can I intelligently plan for the construction of a new data center, MAC’s of IT and facility devices, or technology refreshes?
                                </li>
                                <li>What risks can I avert at the cabinet, white space and facility power levels?
                                </li>
                            </ul>
                            <p class="comp-para">By looking at data center management in totality, solving problems for both IT and facilities departments, we launched one of the first DCIM software platforms in the data center industry.  As our software matured, we re-focused our efforts to address three areas of overall need within the data center, which would direct our development efforts for years to come: </p>
                            <ul class="custom_today">
                                <li>Data Center Management
                                    <ul class="custom_today">
                                        <li>IT and facility asset management</li>
                                        <li>Power consumption analysis and ratings</li>
