                                        <li>Real-time visual updates of floor plans and assets </li>
                                    </ul>
                                </li>
                                <li>Risk Mitigation
                                    <ul class="custom_today">
                                        <li>Fast and detailed reporting</li>
                                        <li>Real-time multi-channel alerts</li>
                                        <li>Planning for device failover</li>
                                    </ul>
                                </li>
