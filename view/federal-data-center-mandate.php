                        <h3 class="inner-heading">HOW IS IT CALCULATED? </h3>
                        <p class="zero_margin">
                            PUE is calculated by dividing the total amount of energy consumed within the data center space (the total Facility Energy) by the total amount of energy consumed by computing equipment (such as servers, network devices, storage arrays etc.) The energy consumed by the data center would include all powered equipment that are not considered computing devices, such as cooling, lighting and power distribution equipment. This calculation should always be 1 or higher, with a perfect
                            calculated value of 1. For example, a data center that consumes 1,000 kilowatts of power but whose computing equipment consumes 800 kilowatts would yield a PUE rating of 1.25 (1,000kW / 800kW). A typical data center PUE rating would be considered efficient at a value of 1.5 or lower; yet, many companies seek to attain ratings far closer to 1.2 or 1.1. 
                        </p>
