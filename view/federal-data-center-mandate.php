                        <p class="p_space">
                            The US Office of Management and Budget dictates that federal data centers must meet a PUE rating of 1.5 or better and must install data center infrastructure management (DCIM) software by 2018.
                            The corresponding initiative, known as the Data Center Optimization Initiative (DCOI), encourages existing data centers to immediately transition to the usage of DCIM software and to begin 
                            incorporating "green" IT technology to meet the 2018 deadline for identified data center 
                            optimization targets. Further, new federal datacenter builds are required to meet a more stringent PUE rating of 1.4 during inception. This mandate stresses the need for both competent DCIM 
                            software and energy practices to achieve either of these PUE objectives, and it views DCIM 
                            software not as an optional extra but as a vital component of the data center to meet such goals.

