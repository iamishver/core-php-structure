                        <h3 class="inner-heading pue-color">What is PUE?</h3>
                        <p class="pue-color">
                            PUE (Power Usage Effectiveness) is a statistic that measures the efficiency of energy usage within a data center. Developed by the Green Grid organization in 2006, PUE is currently the most widely used measurement to determine the effectiveness of power usage within the datacenter
                            environment and is used as the de facto standard in benchmarking any data center against ideal
                            efficiency targets and required goals. Companies such as Google strive to meet or exceed low PUE targets such as 1.21 or below and actively promote better energy practices to drive that goal even lower over the long-term. 

                        </p>

