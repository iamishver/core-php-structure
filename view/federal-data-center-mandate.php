                        <h3 class="inner-heading">HOW CAN NORLINX HELP FEDERAL DATA CENTERS ACHIEVE THE REQUIRED PUE RATINGS? </h3>
                        <p>
                            Much of the groundwork to achieving a target PUE goal of 1.5 or better is accomplished by simply exposing the current power environment to Facilities and IT personnel. Those responsible for the energy efficiency of a data center must first know exactly what power is being consumed in the data center (from both the Facilities and IT sides) and then where any anomalies lie. GSM is designed to provide both visual indicators and customizable alerting mechanisms to communicate this
                            information. GSM can isolate exactly which devices are in danger of exceeding efficiency limits and which devices are no longer serving a valuable function (and yet consuming power).
                            For example, a user can compare various models and configurations of IT devices within GSM to identify those models which consume the most power or that run most inefficiently. Armed with that knowledge, that user can then remedy that situation by either consolidating such devices into 
                            lower-consumption blade systems or by purchasing "greener" variations of that equipment.
                            Time-specific power consumption issues can just as easily be identified through a variety of 
                            historical trend reports within GSM. GSM can also take advantage of a host of smart sensor technologies available on the leading monitored power devices to fine-tune utilization and control of power efficiency and consumption down to the outlet or connected device. GSM also provides tools to optimize the balancing of power loads within the data center, down to the breaker or outlet. The more balanced the power load is across all phases within a cabinet, the more energy efficient the cabinet can become. 
                        </p>
